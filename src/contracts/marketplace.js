export const marketplaceInterface = new utils.Interface(marketplaceAbi)

export const marketplaceContract = new Contract(marketplaceAddress, marketplaceInterface)

export const useMarketplaceFunction = (fnName, options) => useContractFunction(marketplaceContract, fnName, options)

export const useMarketplaceCall = (fnName, args = []) => {
    const { value, error } = useCall({
        contract: marketplaceContract,
        method: fnName,
        args: args
    }) ?? {
        value: undefined,
        error: undefined,
    };

    return {
        value: value && value[0],
        error: error,
    };
};